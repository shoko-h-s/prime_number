# nは素数であるか判定する
n = int(input("素数判定を行う数を入力："))

# nが1以下の場合　→　素数ではない
if n <= 1:
    print(f"{n}は素数ではありません")

# nが2以上の場合 → 2～(n-1)の数で割ってみて、どれかで割れたらループを抜ける
else:
    for i in range(2, n):
        if n % i == 0:
            print(f"{n}は素数ではありません")
            break
        
    # (n-1)までのどの数でも割れない場合 → 素数である
    else:
        print(f"{n}は素数です")
